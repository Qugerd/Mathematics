a = array([[-9, -3, 4, -10],
           [-1, -6, 8, 8],
           [-8, -6, -5, -5],
           [-9, -3, -9, 1]], float)



n = len(a)


u = a
l = zeros((n, n))
r = zeros((n, n))


for i in range(n):
    for j in range(i, n):
        l[j][i] = u[j][i] / u[i][i]

for k in range(1, n):
    for i in range(k - 1, n):
        for j in range(i, n):
            l[j][i] = u[j][i] / u[i][i]

    for i in range(k, n):
        for j in range(k - 1, n):
            u[i][j] = u[i][j] - l[i][k - 1] * u[k - 1][j]

for i in range(n):
    for j in range(n):
        for k in range(n):
            r[i][j] += l[i][k] * u[k][j]

